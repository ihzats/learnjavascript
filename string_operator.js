// operator +
let namaDepan = "Albert";
let namaBelakang = "Einstein";

console.log(namaDepan + " " + namaBelakang); // Output: Albert Einstein

// operator +=
let kata = "Halo ";
kata += "Dunia";

console.log(kata); // Output: Halo Dunia


// contoh string dan number
let kata2 = "Kambing ada ";
let bilangan = 5;

console.log(kata2 + bilangan); // Output: Kambing ada 5